using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Stud
{
    public class Student
    {
        
        
            private int rollno;
            private string name;
            private double perc;

          
            public int Rollno
            {
                get { return rollno; }
                set { rollno = value; }
            }
            public string Name
            {
                get { return name; }
                set { name = value; }
            }
            public double Percentage
            {
                get { return perc; }
                set { perc = value; }
            }
        

    }
}
